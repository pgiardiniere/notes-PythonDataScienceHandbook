# iPython:
# it's basically a REPL (read-eval-print loop) with extras
# it's also the Python kernel for jupyter notebooks

# Jupyter:
# Straight from the text:
# "The Jupyter notebook is a browser-based graphical interface to the IPython shell,
#  and builds on it a rich set of dynamic display capabilities."

# "Though the IPython notebook is viewed and edited through your web browser window, 
#  it must connect to a running Python process (kernel) in order to execute code."

# Recall from article that Jupyter notebooks are compatible with other
# language kernels by default (Julia, R, more are community-supported).

# A single Jupyter notebook may have multiple kernels.

# Launch Jupyter from terminal:
#   jupyter notebook
# this creates a local server for the webapp on port 8888
